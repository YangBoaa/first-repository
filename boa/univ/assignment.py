a = 100
b = 20
result = a + b

print(a, "+", b, "=", bin(result))
print(a, "+", b, "=", oct(result))
print(a, "+", b, "=", result)
print(a, "+", b, "=", hex(result))

result = a - b
print(a, "-", b, "=", bin(result))
print(a, "-", b, "=", oct(result))
print(a, "-", b, "=", result)
print(a, "-", b, "=", hex(result))

result = a * b
print(a, "*", b, "=", bin(result))
print(a, "*", b, "=", oct(result))
print(a, "*", b, "=", result)
print(a, "*", b, "=", hex(result))

result = int(a / b)
print(a, "/", b, "=", bin(result))
print(a, "/", b, "=", oct(result))
print(a, "/", b, "=", result)
print(a, "/", b, "=", hex(result))
