# 1. 키보드 입력을 통해 2이상의 양의 정수를 입력받는다.
# 2. 입력받은 수를 1이상의 정수로 나누어 나머지가 0이 되는 모든 수를 찾는다.
#    단, 입력받은 수 자체는 포함하지 않는다.

# 3. 입력받은 수에 대해 조건 2[2번을 의미함]를 만족하는 모든 수를 list로 담아서 출력한다.
# 4. 입력받은 수에 대해 조건 2를 만족하는 모든 수를 더하여 출력한다.
# 5. 이러한 과정을 무한 반복한다.

# 위의 조건을 만족하는 프로그램을 작성하여 제출하시오.

while True:
    list = []
    a = int(input("2이상의 점수를 입력하세요: "))
    for i in range(1, a):
        if a % i == 0:
            list.append(i)
    list.append(a)
    print(list)

    result = sum(list)
    print(result)
